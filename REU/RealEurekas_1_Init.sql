-- Residual definition of all available boosts
CREATE TABLE REurBoostDefs (
	BoostTypeID	INTEGER NOT NULL PRIMARY KEY, -- new generic Boosts, should be 300+
	BClass		TEXT    NOT NULL,
	TDesc		TEXT    NOT NULL,  -- TriggerDescription
	--TLDesc		TEXT    NOT NULL,  -- TriggerLongDescription - not necessary
	U1Type		TEXT,  -- Unit1Type
	BType		TEXT,
	IType		TEXT,
	BTType		TEXT,
	RType		TEXT,
	NItems		INTEGER NOT NULL DEFAULT 0,
	NItems2		INTEGER NOT NULL DEFAULT 0, -- for custom boosts
	FType		TEXT,						-- FeatureType - for custom boosts
	DType		TEXT,
	RRes		BOOLEAN NOT NULL CHECK (RRes IN (0, 1)) DEFAULT 0,
	BCType		TEXT
);

-- Boost mappings to techs and civics
CREATE TABLE REurMapping (
    BoostID		INTEGER NOT NULL, -- each Tech and Civic has a unique BoostID assigned
	BoostSeq	INTEGER NOT NULL CHECK (BoostSeq IN (0,1,2,3,4)) DEFAULT 0, -- will be chosen randomly using Modulo 5 operation
	BoostTypeID	INTEGER NOT NULL, -- possible to use Boost Types
	FOREIGN KEY (BoostID) REFERENCES Boosts(BoostID) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (BoostTypeID) REFERENCES REurBoostDefs(BoostTypeID) ON DELETE CASCADE ON UPDATE CASCADE);

